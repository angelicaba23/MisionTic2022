#Calcula la superficie de un triángulo en función de la base y la altura (S = ½ × Base × Altura).ntradas


base = float(input("Digite el valor de la base "))
altura = float(input("Digite el valor de la altura "))
#Procesos
superficie = base * altura / 2
#Salidas
print ("La superficie del triángulo es = ", base ," * ",altura ," / 2 = ", superficie)
